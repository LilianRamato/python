colour=input("enter colour:")
# if elif else statement
if colour=="Red" or colour=="red":
    print("i wear red on monday")
elif colour=="blue" or colour=="Blue":
    print("i wear blue on tuesday")
elif colour=="yellow" or colour=="Yellow":
    print("i wear yellow on wednesday")
elif colour=="white" or colour=="White":
    print("i wear white on thursday")
elif colour=="black" or colour=="Black":
    print("i wear black on friday")
else:
    print("i dont have those colours")